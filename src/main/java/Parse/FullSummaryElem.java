package Parse;

public class FullSummaryElem {
    String dt;
    Double pt_in;
    Double wt_in;
    Double cbh_in;

    public String getDt() {
        return dt;
    }

    public void setDt(String dt) {
        this.dt = dt;
    }

    public Double getPt_in() {
        return pt_in;
    }

    public void setPt_in(Double pt_in) {
        this.pt_in = pt_in;
    }

    public Double getWt_in() {
        return wt_in;
    }

    public void setWt_in(Double wt_in) {
        this.wt_in = wt_in;
    }

    public Double getCbh_in() {
        return cbh_in;
    }

    public void setCbh_in(Double cbh_in) {
        this.cbh_in = cbh_in;
    }
}
